function add(a , b){
    return (a+b);
}

function subtract(a , b){
    return (a-b);
}

function multiply(a , b){
    return (a*b);
}

function divide(a, b){
    return (a/b);
}

function increment(n){
    return (n += 1);               //can do (n += 1) OR (++n)
}

function decrement(n){
    return (n -= 1);                  //can do (n -= 1) OR (--n)
}

function makeInt(n){
    return (parseFloat(n));  
}

function preserveDecimal(n){
    return (parseFloat(n));
}

